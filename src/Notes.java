
public class Notes {
	protected String title;
	protected String note;
	protected int month;
	protected int day;
	protected int year;
	protected String back;
	
	public Notes (String t, String n, int m, int d, int y, String b) {
		title = t;
		note = n;
		month = m;
		day = d;
		year = y;
		back = b;
	}
	
	
}
